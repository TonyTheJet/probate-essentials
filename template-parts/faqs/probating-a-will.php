	<h4>How long does informal probate take?</h4>
	<div>
		Informal probate can take as little as 2 to 3 weeks, depending on the particular judge.  Sometimes, if there are various complications in contacting beneficiaries or heirs, or other complications arise, informal probate can take as long as three or four months.
	</div>

	<h4>Do all the beneficiaries need to be contacted?</h4>
	<div>
		Yes. The named or first level beneficiaries listed in a Will must all be notified about the informal probate proceedings.
	</div>

	<h4>Do the beneficiaries need to appear in court?</h4>
	<div>
		No.
	</div>

	<h4>What if my benefactor died without a will?</h4>
	<div>
		If your benefactor died without a will, you beneficiaries will especially need to go through informal probate, asking the judge to appoint a personal representative to administer your benefactor's estate.
	</div>

	<h4>Are there any conflicts involved in probating a will?</h4>
	<div>
		Sometimes there are. If conflicts arise, the family then faces mediation or arbitration if the planning documents wisely require mediation or arbitration. Otherwise, the family faces probate litigation.
