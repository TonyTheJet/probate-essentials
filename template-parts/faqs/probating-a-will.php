	<h4>What is probate?</h4>
	<div>
		<p>
			The word probate comes from the Latin term for probere: to prove. In the law, probate is associated with inheritance issues. You can read many definitions of the word probate, but we think our definition may be the easiest to remember:
		</p>
		<div class="tabbed"><strong>Probate</strong> is the legal</div>
		<div class="tabbed"><strong>Process of</strong></div>
		<div class="tabbed"><strong>Proving who is entitled to control and take</strong></div>
		<div class="tabbed"><strong>Possession of</strong></div>
		<div class="tabbed"><strong>Property after the owner's incapacity or death.</strong></div>
		<p>
			This definition is easy to remember.  Five &quot;p's&quot; and five &quot;pro's&quot;: Probate, Process, Proving, Possession, Property. Property includes real property (that is, real estate and associated homes or buildings). The word property also includes bank accounts, investment accounts, life insurance policies, vehicles, boats, and other assets.  Property further includes a variety of things such as books, tools, collections, musical instruments, and other personal property.
		</p>
	</div>

