	<h4>What is the difference between formal and informal probate?</h4>
	<div>
		<p>Informal probate is the court process by which the validity of a will is proven and a personal representative (&quot;PR&quot;) is appointed. Informal probate generally does not involve any conflicts among the beneficiaries and fiduciaries. That is everyone agrees there is a will, but a third party is demanding that the validity of the will be established by a court or that a PR be appointed.</p>
		<p>Putting aside technical definitions, formal probate is probate litigation: a conflict in a court of law among beneficiaries and fiduciaries regarding the validity of a will and often numerous other issues regarding control of a person's assets.</p>
	</div>

	<h4>Why to I need to "probate a will"?</h4>
	<div>
		<p>If there is ever any question about the validity of a will in the mind of a third party (a bank, credit union, investment company, life insurance company, or any other third-party), the will must be probated. In other words, the validity of the will must be proven to a court of law. After a court approves the validity of the will, the third party will then be comfortable in accepting the will.</p>
		<p>Further, if there is ever any question in the mind of a third party about who is authorized to represent a deceased person, then a court must be petitioned to appoint a person who will be authorized to represent the deceased person. Only after a court appoints a person as the &quot;personal representative&quot; (known in other states as an executor), will the third party feel comfortable in working with that person.</p>
	</div>

	<h4>Who can be appointed a personal representative?</h4>
	<p>Most often, those most closely related to the deceased individual are authorized and appointed as the deceased person's personal representative.</p>

