	<h4>How do I prevent <i>informal</i> probate?</h4>
	<div>
		<p>How can I prevent informal probate?</p>
		<ol>
			<li>You can give away all your assets before you die.</li>
			<li>You can name your beneficiaries as co-owners of your assets.</li>
			<li>You can change your ownership documents and name your beneficiaries the beneficiaries of your assets.</li>
			<li>You can create a trust, and name your trust on your ownership documents as the owner or beneficiary of all your assets.</li>
		</ol>
		<p>Each of these options carries either significant costs or risks. It is most wise to consult with an estate planning attorney before choosing any of these options.</p>
