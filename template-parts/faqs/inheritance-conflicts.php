	<h4>What is probate?</h4>
	<div>
		<p>
			The word probate comes from the Latin term for probere: to prove. In the law, probate is associated with inheritance issues. You can read many definitions of the word probate, but we think our definition may be the easiest to remember:
		</p>
		<div class="tabbed"><strong>Probate</strong> is the legal</div>
		<div class="tabbed"><strong>Process of</strong></div>
		<div class="tabbed"><strong>Proving who is entitled to control and take</strong></div>
		<div class="tabbed"><strong>Possession of</strong></div>
		<div class="tabbed"><strong>Property after the owner's incapacity or death.</strong></div>
		<p>
			This definition is easy to remember.  Five &quot;p's&quot; and five &quot;pro's&quot;: Probate, Process, Proving, Possession, Property. Property includes real property (that is, real estate and associated homes or buildings). The word property also includes bank accounts, investment accounts, life insurance policies, vehicles, boats, and other assets.  Property further includes a variety of things such as books, tools, collections, musical instruments, and other personal property.
		</p>
	</div>
	<h4>What is probate litigation?</h4>
	<div>
		<p>
			Probate litigation is a conflict in a court of law about inheritances or control of a person's assets. Probate litigation involves
		</p>
		<ul>
			<li>Conflicts about the validity of a will or trust.</li>
			<li>Disputes with a fiduciary about how assets are being administered.</li>
			<li>Arguments about the interpretation of a provision in a will or trust.</li>
			<li>Conflicts involving the use of a power of attorney.</li>
			<li>Disputes regarding the appointment of a guardian or conservator.</li>
			<li>Arguments regarding inheritance abuse and fraud against a vulnerable parent.</li>
			<li>Second marriage conflicts.</li>
		</ul>
		<p>
			Again, probate litigation is a conflict in a court of law about inheritances or control of a person's assets.
		</p>
	</div>

	<h4>How much does probate litigation cost?</h4>
	<div>
		<p>
			Once a complaint or petition is filed with a court of law, probate litigation has begun. And once probate litigation begins, it is unavoidably expensive.
		</p>
		<p>
			Most probate attorneys bill their time at $200 to $350 per hour. And most probate cases require 80 to 100 hours of an attorney's time.
		</p>
		<p>
			Any attorney promising to resolve a typical probate litigation case for less than $15,000 is not being honest. In our 25 years of experience, each party in probate litigation will pay an average of $15,000-$20,000 to fight their case. A small percentage of cases cost less than $15,000, some as little as $5,000. And a small percentage of cases cost more than $20,000, much more. But approximately 70% to 80% of cases cost each party in the range of $15,000 to $20,000.
		</p>
		<p>
			Keep in mind that a good attorney will always be less expensive than &quot;bulldog&quot; attorneys. Bulldog-type attorneys always and unnecessarily drive up costs. A good attorney is tough, but knows how to effectively resolve cases, while efficiently keeping litigation costs as low as possible.
		</p>
	</div>

	<h4>Do you take fees on contingency?</h4>
    <div>
        <p>No. Further, we do not know of one competent attorney who does probate litigation on a contingency fee basis.</p>
        <p>Probate litigation is not personal injury litigation. Siegfried & Jensen and other personal injury attorneys take cases on a contingency basis, because personal injury cases are almost always fights against a negligent individual's insurance company, and the attorneys' fees are guaranteed to be paid when the insurance company settles.</p>
        <p>Probate litigators do not have the luxury of having a deep-pocket insurance company paying their fees. For this reason alone, probate litigators must be paid as they do their work.</p>
    </div>

	<h4>How long does probate litigation take?</h4>
	<div>
		<p>We have found that a small percentage of probate litigation cases take 3 to 5 months to resolve. Another small percentage of cases last longer than 16 months. In our 25 years of experience, most probate litigation cases last from 5 to 16 months. The length of a case depends entirely on the following:</p>
		<ul>
			<li>the willingness of each party to settle;</li>
			<li>the intelligence and willingness of the attorneys to control their clients and resolve the case;</li>
			<li>the experience of a judge in probate litigation; and</li>
			<li>the control a judge has over his or her court in this unique type of litigation.</li>
		</ul>
		<p>Finally, no one can escape the Utah procedural rules that allow both sides to take weeks and months to file various pleadings or conduct various acts, such as questioning witnesses.</p>
	</div>

	<h4>Do no-contest clauses prevent inheritance conflicts?</h4>
	<div>
		No. The answer here is absolutely NO. There is no study proving that no contest clauses actually work in preventing inheritance conflicts. Further, judges reject no contest clauses in over 80% of the cases involving no contest clauses. Do not be deterred by a no contest clause. We routinely and quickly slice through a no-contest clause. If you have a good case, the no contest clause will fail under Utah law.
	</div>

	<h4>Do you represent trustees and executors?</h4>
	<div>
		<p>Yes. We represent trustees and executors. (In Utah, we call executors &quot;personal representatives&quot; or &quot;PRs.&quot; Further, trustees and executors are more generally known as &quot;fiduciaries.&quot;</p>
