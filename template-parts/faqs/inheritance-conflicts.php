	<h4>Can you force the other side to pay attorneys' fees?</h4>
	<div>
		This is very difficult to do. Most Utah judges are reluctant to force the other side to pay attorneys' fees. It happens, but very rarely. We always ask, but even in our successful cases, the judge invariably refuses to make the losing party pay.
	</div>

	<h4>What is inheritance fraud?</h4>
	<div>
		Inheritance fraud is any fraudulent attempt by a beneficiary, trustee, agent, or even a friend to undermine a person's will, trust, power of attorney. Inheritance fraud is any attempt to dishonestly take assets belonging to a benefactor.
	</div>

	<h4>What is undue influence?</h4>
	<div>
		Undue influence occurs when a manipulator attempts to override another person's desires and replace the other person's desires with the manipulator's desires. Undue influence is difficult to prove (mere allegations are inadequate, even though they may be based in fact). But we have successfully won cases based on proven undue influence.
	</div>

	<h4>What is mental incapacity?</h4>
	<div>
		<p>The Utah Probate Code (§ 75-1-201(22)) and the Utah Uniform Power of Attorney Act (§ 75-9-102(5)), define mental incapacity generally as follows:</p>
		A person is mentally incapacitated if their ability to
		<ul>
			<li>receive and evaluate information;</li>
			<li>make and communicate decisions; or</li>
			<li>provide for necessities such as food, shelter, clothing, health care, or safety</li>
		</ul>
		<p>is impaired, such that the person is unable, even with technological assistance, to safely and appropriately protect themselves and conduct their affairs.</p>
	</div>

