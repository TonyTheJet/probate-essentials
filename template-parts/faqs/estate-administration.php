	<h4>What is involved in administering an estate?</h4>
	<div>
		<p>Most books for dummies on administering an estate are over 360 pages long. That should indicate that administering an estate is not a job to be taken lightly. It takes a dedicated and smart person to read and understand these books and apply all the rules.	In brief, dozens of legal, tax, and financial issues are involved in</p>
		<ul>
			<li>taking control of a deceased persons assets;</li>
			<li>following the law;</li>
			<li>complying with the requirements of a detailed will or trust;</li>
			<li>managing and protecting assets;</li>
			<li>satisfying obligations;</li>
			<li>gathering and selling assets, and distributing assets;</li>
		</ul>
		<p>The failure of family members to appropriately administer an estate or trust is one of the primary causes of anger, resentment, and probate litigation.</p>
	</div>

	<h4>Why would I want a professional to administer my trust or estate?</h4>
	<div>
		An independent, qualified accountant, attorney, or trustee associated with a bank or credit union are professionals who—if experienced—are qualified to administer an estate. An independent, qualified professional will save your family significant amounts of money in doing things right the first time.
		Professionals know how to effectively ensure your final obligations are appropriately satisfied. Professional deals with taxes, making sure tax work is done promptly, efficiently, and correctly. Professionals know how to economically deal with third parties and a host of various issues in managing, protecting, selling, investing, and distributing your assets. A good professional will send regular quarterly accountings to your named beneficiaries, with copies of all account statements, invoices, and receipts, proving how the trust or estate is being administered. Everything is in the open.
	</div>

	<h4>Who can administer my estate?</h4>
	<div>
		<p>Any competent person over 21 years of age can administer your estate. That does not mean they will do a good job. Those who do a particularly poor job are</p>
		<ul>
			<li>an adult family member (however brilliant) who is busy with his or her own career and family;</li>
			<li>who is not trained in the legal aspects of estate administration; and</li>
			<li>who is arrogant enough to believe that it couldn't be that difficult.	</li>
		</ul>
		<p>It is these family members who directly cause resentment, anger, conflicts, and probate litigation.</p>
	</div>


	<h4>What are my options in having a professional administer my estate?</h4>
	<div>
		Many of our estate planning clients choose to have us help their beneficiaries or fiduciaries administer an estate for the first 3 to 6 months after the client's death. This allows us to help the family fiduciary get off to the right start. After that the family fiduciary takes over. That is just one among numerous options you have in retaining us as professionals to help administer your estate.
	</div>

	<h4>What does it cost?</h4>
	<div>
		<p>The most time-consuming aspects of trust and estate administration occur in the first four (4) to (12) months after your death. During this time we apply our hourly attorney rates. Depending on the complexity (not the value, but the complexity) of the trust or estate, these initial administrative fees may be as little as $8,000.</p>
		<p>That may seem high. Two points. First, these fees are much less expensive than probate litigation costs caused by inexperienced or dishonest family fiduciaries administering an estate. Second, diligent and honest busy family members acting as fiduciaries will spend many, many scores of hours administering your estate. A professional's fees allow your busy family members to grieve and administer to their own families and personal affairs.</p>
		<p>After the initial administrative expenses, our administration fees average less than 1% of the total value of a client's assets each year. (The average is actually seven tenths of 1%, or 0.007%)</p>
		<p>So, if a client has $100,000 worth of assets at their death, our administration fees would be $700 on average each year. If a client has $1 million worth of assets at their death, our administration fees would be $7,000 on average each year.</p>
		<p>This is significantly less than rates charged by professionals at bank trust departments, and the level of personal service towards beneficiaries is much better at our small firm than at a national bank.</p>
	</div>

